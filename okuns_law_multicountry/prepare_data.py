# Compute quarterly average unemployment


def quarter_avg(year, q):
    months = [(q-1)*3, (q-1)*3+1, (q-1)*3+2]
    vals = [unrate_monthly[year][m] for m in months]
    vals = [v for v in vals if v is not None]
    return sum(vals) / len(vals) if vals else None


# Build dataset
rows = []
for year, q, gdp_val in gdp:
    unemp = quarter_avg(year, q)
    if unemp is None:
        continue
    quarter_str = f"{year}Q{q}"
    rows.append({
        'quarter': quarter_str,
        'year': year,
        'q': q,
        'gdp': gdp_val,
        'unemp': round(unemp, 4),
        't': len(rows) + 1  # time index
    })

